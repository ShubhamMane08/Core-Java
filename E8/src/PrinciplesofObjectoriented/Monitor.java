package PrinciplesofObjectoriented;

public class Monitor {
	String brand;
	double resolution;
	int size;
	double price;
	
	
	
	Monitor()
	{
		
	}
	public Monitor(String brand, double resolution, int size,double price)
	{
		this.brand=brand;
		this.resolution=resolution;
		this.size=size;
		this.price=price;
	}

}
