class Addition 
{
	public static void main(String[] args) 
	{

		byte a=10;
		byte b=20;
		int c= a+b;
		System.out.println("Addition of two byte variables is integer : "+c);

		short d =30;
		short e=40;
		int f=d+e;
		System.out.println("Addition of two short data types is integer : "+f);

		int w=10;
		int q=20;
		int s=w+q;
		System.out.println("Addition of two Integers is Integer : "+s);

		long lg=9854658783l;
		long nx=9856789546l;
		long res=lg+nx;
		System.out.println("Addition of two long data types is Long value : "+res);

		float ab=20.21f;
		float cb=30.1f;
		float ans=ab+cb;
		System.out.println("Addition of two floating number is Float : "+ans);

		double er=20.21d;
		double re=30.1d;
		double rese=er+re;
		System.out.println("Addition of two Double datatype is Double : "+rese);

		String str1="Qspiders";
		String str2="Jspiders";
		String str3=str1+str2;
		System.out.println("Addition of two types of String is String : "+str3);

		
	}
}
