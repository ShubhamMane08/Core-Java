class Subtraction 
{
	public static void main(String[] args) 
	{

		byte a=10;
		byte b=8;
		int c=a-b;
		System.out.println("Subtraction of two byte value is Integer : "+c);

		short a1=200;
		short a2=500;
		int a3=a1-a2;
		System.out.println("Subtraction of two short value is Integer : "+a3);

		char c1='a';
		char c2='b';
		int c3=c1-c2;
		System.out.println("Subtraction of two character value is Integer: "+c3);
		

		int b1=2000;
		int b2=9000;
		int b3= b2-b1;
		System.out.println("Subtraction of two Integer value is Integer "+b3);

		long d1=250l;
		long d2=25000l;
		long d3=d2-d1;
		System.out.println("Subtraction of two Long value is Long : "+d3);

		float e1=1.1f;
		float e2=2.2f;
		float e3=e2-e1;
		System.out.println("Subtraction of two Floating Number is Float : "+e3);

		double r1=1.1;
		double r2=2.2;
		double r3=r2-r1;
		System.out.println("Subtraction of two Double Value is Double : "+ r3);

		//String s1="Qspiders";
		//String s2="Jspiders";
		//String s3=s2-s1;
		//System.out.println("SUbtraction of two Strings is : "+s3);
	}
}
