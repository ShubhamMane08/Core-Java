class Switch 
{
	public static void main(String[] args) 
	{
		int ip=1;
		switch(ip)
		{
			case 1:
			{
				System.out.println("We are in Case 1");
			}
			break;
			case 2:
		{
			System.out.println("We are in Case 2");
			break;
		}
		case 3:
		{
			System.out.println("We are in Case 3");
			break;
		}
		default :
		{
			System.out.println("We are in Default Case..!!");
		}
		}




	}
}
